/*
For:

Syntax:
    for(a = 5; a < 10; a++) { .. }
    a = 5 is called initialization expression
    a < 10 is called conditional/test expression
    a ++ is called update expression
*/

for (a = 5; a < 10; a++) {
    console.log(a);
}

/*
While:

Syntax:
    while(condition) { .. }
*/

a = 1;
while (a < 10) {
    console.log(a);
    a++;
}